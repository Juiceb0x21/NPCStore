
class Producto():
    def __init__(self, id, nombre, descripcion, imagen, precio, stock, categoria_id, marca_id):
        self.id = id
        self.nombre = nombre
        self.descripcion = descripcion
        self.imagen = imagen
        self.precio = precio
        self.stock = stock
        self.categoria_id = categoria_id
        self.marca_id = marca_id